package ru.webstr.app;

public class Calculator {
    public Integer sum(Integer a, Integer b) {
        return a + b;
    }

    public Integer minus(Integer i, Integer i1) {
        return i - i1;
    }

    public Integer multiply(Integer i, Integer i1) {
        return i * i1;
    }

    public Integer devide(Integer i, Integer i1) {
        return i / i1;
    }

    public Integer remain(Integer i, Integer i1) {
        return i % i1;
    }
}
